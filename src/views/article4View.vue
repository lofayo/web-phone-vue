<br>  <br>  

除了电子产品的零售和这种高档百货店，很多大型的服装、化妆品零售商也在纷纷寻求Mobile POS方案以便促进门店的高效管理、提升顾客体验、增加销售和有效利用店内的每一寸宝贵空间。然而不同Apple零售店，大型零售客户在引进 Mobile POS 方案上面临诸多挑战，Apple零售店的系统架构在引进Mobile POS的时候都是从零而建，在系统上没有任何遗留的“负担”。
