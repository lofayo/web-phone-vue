<br>  <br>  
同时店内服务人员可以通过Mobile POS查询到任何其他门店及线上渠道的库存信息，即使本店缺货也能随时快速为顾客查询到其他渠道的库存量，保证不流失任何一个销售机会。诺德斯特龙引进 Mobile POS的初衷可能更多在于：希望在店内的任何一个角落都可以将每个顾客“照顾周到“，在试衣间试完衣服或者鞋子，就可以立马完成之后的所有流程，而不需要让顾客等待，不需要让他们特地移步到收银台，因为服务的效率及方便性对于顾客来说越来越重要。
