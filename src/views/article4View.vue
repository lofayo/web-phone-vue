<br>  <br>  
Apple零售店在几年前就意识到了这个问题和传统收银台的缺陷，早在2011年，Apple零售店就部署了Mobile POS 的运用，店内的员工人手一台 Mobile POS（由 iPod Touch 和 支付终端背夹组成），使每个店员可以在店内的任何一个角落为顾客提供支付结账服务。同时在这样一个先进、灵活的系统平台和智能终端上，Apple 零售店将店内涉及的人、财、物等管理集成一体化，为顾客打造无缝的购物体验，同时也大大提高了员工的服务效率。
